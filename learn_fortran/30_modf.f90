!-----------------------------------------------------------------------------------------------------------------------------------
module modf
  implicit none

contains
!-------------------------------------------------------------------------------
  subroutine fvec(d,x,t,fv)
    implicit none
    integer :: d
    real(8) :: x(d), fv(d), t
    real(8) :: ax, ay, vx0, vy0, vx, vy

    ax = 0;  vx0 = 5;  vx = vx0 + ax*t
    ay = -10;  vy0 = 20;  vy = vy0 + ay*t

    fv(1) = vx;  fv(2) = vy

  end subroutine
!-------------------------------------------------------------------------------
end module
